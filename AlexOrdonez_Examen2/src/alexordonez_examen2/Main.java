    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        Naves nav;
        if (r_naves.isSelected()) {
            String despeje = JOptionPane.showInputDialog("Ingrese el lugar de despeje");
            nav = new Tripulada(despeje, Double.parseDouble(nav_vel.getText()), Integer.parseInt(nav_ser.getText()), (Planeta) cb_planetas.getSelectedItem());
            tripu=(Tripulada)nav;
            jDialog3.setModal(true);
            jDialog3.pack();
            jDialog3.setLocationRelativeTo(this);
            jDialog3.setVisible(true);
        } else {
            String material = JOptionPane.showInputDialog("Ingrese el material");
            double peso = Double.parseDouble(JOptionPane.showInputDialog("Ingrese el peso"));
            nav = new Sonda_E(material, peso, Double.parseDouble(nav_vel.getText()), Integer.parseInt(nav_ser.getText()), (Planeta) cb_planetas.getSelectedItem());
        }
        JOptionPane.showMessageDialog(this, "nave creada");
        DefaultComboBoxModel model = (DefaultComboBoxModel) cb_naves.getModel();
        model.addElement(nav);
        cb_naves.setModel(model);
    }//GEN-LAST:event_jButton3MouseClicked

