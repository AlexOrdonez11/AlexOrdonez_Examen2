    public void cargar() {
        File archivo=new File("./planetas.planet");
        if(archivo.exists()){
        try {
            planets = new ArrayList();
            Planeta temp;
            FileInputStream entrada = new FileInputStream(archivo);
            ObjectInputStream objeto = new ObjectInputStream(entrada);
            try {
                while ((temp = (Planeta) objeto.readObject()) != null) {
                    planets.add(temp);
                }
            } catch (EOFException e) {
                //encontro el final del archivo
            }
            objeto.close();
            entrada.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
        }
        File archivo1=new File("./astronautas.astro");
        if(archivo1.exists()){
        try {
            astros = new ArrayList();
            Astronautas temp;
            FileInputStream entrada = new FileInputStream(archivo1);
            ObjectInputStream objeto = new ObjectInputStream(entrada);
            try {
                while ((temp = (Astronautas) objeto.readObject()) != null) {
                    astros.add(temp);
                }
            } catch (EOFException e) {
                //encontro el final del archivo
            }
            objeto.close();
            entrada.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
        }
        for (Astronautas a : astros) {
            Object[]o={a.getNombre(),a.getExperiencia(),a.getNacionalidad(),""+a.getSueldo(),a.getSexo(),""+a.getPeso()};
            DefaultTableModel model=(DefaultTableModel)jt_astros.getModel();
            model.addRow(o);
            jt_astros.setModel(model);
        }
        for (Planeta p : planets) {
            Object[]o={p.getNombre(),""+p.getTemp(),""+p.isAnillos(),p.getSuperficie(),""+p.getDistancia()};
            DefaultTableModel model=(DefaultTableModel)jt_planetas.getModel();
            model.addRow(o);
            jt_planetas.setModel(model);
        }
        for (Planeta p : planets) {
            DefaultComboBoxModel model = (DefaultComboBoxModel) cb_planetas.getModel();
            model.addElement(p);
            cb_planetas.setModel(model);
        }
        for (Astronautas p : astros) {
            DefaultComboBoxModel model = (DefaultComboBoxModel) cb_astronautas.getModel();
            model.addElement(p);
            cb_astronautas.setModel(model);
        }
    }
